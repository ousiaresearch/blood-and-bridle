#!/usr/bin/env node
/**
 * generate-portraits.mjs
 * Generates horse portraits using FAL's gpt-image-2
 */

import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const MANIFEST_PATH = path.resolve(ROOT, 'scripts', 'portraits-manifest.json');
const OUTPUT_DIR = path.resolve(ROOT, 'assets', 'horses');
const INDEX_PATH = path.resolve(OUTPUT_DIR, 'index.js');

const FAL_KEY = process.env.FAL_KEY;
if (!FAL_KEY) {
  console.error('FAL_KEY environment variable required');
  process.exit(1);
}

const FAL_ENDPOINT = 'https://fal.run/fal-ai/openai/gpt-image-2';

const STYLE_ANCHOR = `
A portrait of a single horse, standing in dust-lit golden hour light on a Pacific Northwest ranch.
Loose painterly brushstrokes, warm amber and umber tones, Western ranch atmosphere.
The horse fills the frame -- no busy background, just suggestion of arena dust and cedar fence posts.
Style: contemporary Western realism, not photorealistic, not cartoon.
Mood: the quiet dignity of a working animal who knows its worth.
`.trim();

async function generatePortrait(spec) {
  const prompt = `${STYLE_ANCHOR}\n\nSubject: ${spec.subject}. ${spec.details}`;
  
  const body = {
    prompt,
    quality: 'high',
    size: '1024x1024',
    n: 1
  };

  const res = await fetch(FAL_ENDPOINT, {
    method: 'POST',
    headers: {
      'Authorization': `Bearer ${FAL_KEY}`,
      'Content-Type': 'application/json'
    },
    body: JSON.stringify(body)
  });

  if (!res.ok) {
    const err = await res.text();
    throw new Error(`FAL API error: ${res.status} ${err}`);
  }

  const data = await res.json();
  const imageUrl = data.images?.[0]?.url;
  if (!imageUrl) throw new Error('No image URL in response');

  const imgRes = await fetch(imageUrl);
  if (!imgRes.ok) throw new Error(`Failed to download image: ${imgRes.status}`);
  
  const buffer = Buffer.from(await imgRes.arrayBuffer());
  const outPath = path.resolve(OUTPUT_DIR, `${spec.id}.png`);
  fs.writeFileSync(outPath, buffer);
  
  console.log(`  ✓ ${spec.id} → ${outPath}`);
  return { id: spec.id, path: `/assets/horses/${spec.id}.png`, spec };
}

async function main() {
  if (!fs.existsSync(MANIFEST_PATH)) {
    console.error(`Manifest not found: ${MANIFEST_PATH}`);
    process.exit(1);
  }

  const manifest = JSON.parse(fs.readFileSync(MANIFEST_PATH, 'utf8'));
  
  if (!fs.existsSync(OUTPUT_DIR)) {
    fs.mkdirSync(OUTPUT_DIR, { recursive: true });
  }

  console.log(`Generating ${manifest.length} portraits...`);

  const results = [];
  for (const spec of manifest) {
    try {
      const result = await generatePortrait(spec);
      results.push(result);
      await new Promise(r => setTimeout(r, 1000));
    } catch (err) {
      console.error(`  ✗ ${spec.id} failed: ${err.message}`);
    }
  }

  const indexContent = `// Auto-generated by generate-portraits.mjs
// DO NOT EDIT DIRECTLY -- run the script to regenerate

export const PORTRAITS = ${JSON.stringify(results, null, 2)};

export function findPortrait(horse) {
  const stage = horse.stageId || 'campaigner';
  const temperament = (horse.temperament || '').toLowerCase();
  
  let mood = 'calm';
  if (temperament.includes('hot') || temperament.includes('fierce') || 
      temperament.includes('explosive') || temperament.includes('dominant') ||
      temperament.includes('brilliant') || temperament.includes('protective')) {
    mood = 'intense';
  } else if (temperament.includes('noble') || temperament.includes('proud') ||
             temperament.includes('majestic') || temperament.includes('commanding')) {
    mood = 'proud';
  }

  const candidates = PORTRAITS.filter(p => p.spec.lifeStage === stage);
  if (candidates.length === 0) {
    return PORTRAITS.find(p => p.spec.lifeStage === 'campaigner') || PORTRAITS[0];
  }
  
  const moodMatch = candidates.find(p => p.spec.mood === mood);
  if (moodMatch) return moodMatch;
  
  return candidates[0];
}

export function getPortraitUrl(horse) {
  const portrait = findPortrait(horse);
  return portrait?.path || null;
}
`;

  fs.writeFileSync(INDEX_PATH, indexContent);
  console.log(`\n✓ Generated ${results.length} portraits`);
  console.log(`✓ Wrote index to ${INDEX_PATH}`);
}

main();